/**
 * Second half of codegen for the Sherlock typed vocabulary:
 *   sherlockContract.schema.json  -->  sherlockContract.ts
 *                                 -->  sherlockContract.validator.ts
 *
 * The Python half (``generate_sherlock_contract.py``) emits the JSON Schema.
 * Node consumes it to produce deterministic TS types and a precompiled ajv
 * validator so the frontend has a single ajv-checked contract for every
 * SherlockPart arm (user_message, subtask, tool, retry, assistant_text,
 * reasoning, chart, evidence, error, compaction, step_start, step_finish).
 *
 * Determinism gate: re-running the pipeline must produce a zero ``git diff``
 * — both tools are version-pinned in package.json devDependencies.
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { compile } from 'json-schema-to-typescript';
import Ajv from 'ajv';
import standaloneCode from 'ajv/dist/standalone/index.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..', '..');
const GENERATED_DIR = resolve(REPO_ROOT, 'src', 'features', 'sherlock', 'generated');
const SCHEMA_PATH = resolve(GENERATED_DIR, 'sherlockContract.schema.json');
const TS_TYPES_PATH = resolve(GENERATED_DIR, 'sherlockContract.ts');
const VALIDATOR_PATH = resolve(GENERATED_DIR, 'sherlockContract.validator.ts');

const HEADER = `/**
 * AUTO-GENERATED by scripts/codegen/generate_sherlock_contract.js.
 * Do not edit by hand. Run \`npm run codegen:sherlock-contract\` to regenerate.
 */
/* eslint-disable */
`;

async function main() {
  mkdirSync(GENERATED_DIR, { recursive: true });
  const schema = JSON.parse(readFileSync(SCHEMA_PATH, 'utf8'));

  // ── TypeScript types ─────────────────────────────────────────────
  const ts = await compile(schema, 'SherlockPart', {
    bannerComment: HEADER.trim(),
    style: { singleQuote: true, semi: true },
    enableConstEnums: false,
    strictIndexSignatures: true,
    additionalProperties: false,
  });
  const tsOut = ts.endsWith('\n') ? ts : ts + '\n';
  writeFileSync(TS_TYPES_PATH, tsOut);
  console.log(
    `wrote src/features/sherlock/generated/sherlockContract.ts (${tsOut.length} bytes)`,
  );

  // ── Precompiled ajv validator (standalone, no runtime compile) ───
  const ajv = new Ajv({
    strict: false,
    allErrors: false,
    code: { source: true, esm: true },
  });
  const validate = ajv.compile(schema);
  const moduleSource = standaloneCode.default(ajv, validate);

  const typedSource =
    HEADER +
    '// @ts-nocheck\n' +
    "import type { SherlockPart } from './sherlockContract';\n\n" +
    moduleSource +
    '\n\n' +
    'const _validate = validate as (data: unknown) => boolean;\n\n' +
    'export function validateSherlockPart(raw: unknown): raw is SherlockPart {\n' +
    '  return _validate(raw);\n' +
    '}\n';
  writeFileSync(VALIDATOR_PATH, typedSource);
  console.log(
    `wrote src/features/sherlock/generated/sherlockContract.validator.ts (${typedSource.length} bytes)`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
