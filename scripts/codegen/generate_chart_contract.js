/**
 * Phase 6 §739, §741 — second half of codegen:
 *   chartContract.schema.json  -->  chartContract.ts
 *                              -->  chartContract.validator.ts
 *
 * The Python half (``generate_chart_contract.py``) emits the JSON Schema.
 * Node consumes it to produce deterministic TS types and a precompiled
 * ajv validator so the frontend has a single ajv-checked contract.
 *
 * Determinism is a Phase-6 acceptance gate (§764): both tools are
 * version-pinned in package.json devDependencies.
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { compile } from 'json-schema-to-typescript';
import Ajv from 'ajv';
import standaloneCode from 'ajv/dist/standalone/index.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..', '..');
const GENERATED_DIR = resolve(REPO_ROOT, 'src', 'features', 'chat-widget', 'generated');
const SCHEMA_PATH = resolve(GENERATED_DIR, 'chartContract.schema.json');
const TS_TYPES_PATH = resolve(GENERATED_DIR, 'chartContract.ts');
const VALIDATOR_PATH = resolve(GENERATED_DIR, 'chartContract.validator.ts');

const HEADER = `/**
 * AUTO-GENERATED by scripts/codegen/generate_chart_contract.js.
 * Do not edit by hand. Run \`npm run codegen:chart-contract\` to regenerate.
 */
/* eslint-disable */
`;

// ``ChartReasonCode`` / ``KpiFormat`` are exported from ``types.ts`` as
// hand-written string-literal unions — they stay stable across Pydantic
// rename noise. Structural types (``ChartPayload`` variants,
// ``ChartSummaryField``, ``ChartTableColumn``) land here verbatim.

async function main() {
  mkdirSync(GENERATED_DIR, { recursive: true });
  const schema = JSON.parse(readFileSync(SCHEMA_PATH, 'utf8'));

  // ── TypeScript types ─────────────────────────────────────────────
  const ts = await compile(schema, 'ChartPayload', {
    bannerComment: HEADER.trim(),
    style: { singleQuote: true, semi: true },
    enableConstEnums: false,
    strictIndexSignatures: true,
    additionalProperties: false,
  });
  const tsOut = ts.endsWith('\n') ? ts : ts + '\n';
  writeFileSync(TS_TYPES_PATH, tsOut);
  console.log(
    `wrote src/features/chat-widget/generated/chartContract.ts (${tsOut.length} bytes)`,
  );

  // ── Precompiled ajv validator (standalone, no runtime compile) ───
  const ajv = new Ajv({
    strict: false,
    allErrors: false,
    code: { source: true, esm: true },
  });
  const validate = ajv.compile(schema);
  const moduleSource = standaloneCode.default(ajv, validate);

  // ajv's ESM codegen emits ``import * as ...`` / ``export const validate``.
  // Prefix with the canonical banner + a named export + a type guard so
  // the rest of the frontend uses ``validateChartPayload`` as a drop-in
  // replacement for the old hand-written ``isChartPayload``.
  // The ajv-generated blob is a dense single-line function we do not
  // want tsc to descend into. The public surface we care about is
  // ``validateChartPayload`` — add ``// @ts-nocheck`` so tsc skips the
  // ajv internals while still type-checking our exported helper.
  const typedSource =
    HEADER +
    '// @ts-nocheck\n' +
    "import type { ChartPayload } from './chartContract';\n\n" +
    moduleSource +
    '\n\n' +
    'const _validate = validate as (data: unknown) => boolean;\n\n' +
    'export function validateChartPayload(raw: unknown): raw is ChartPayload {\n' +
    '  return _validate(raw);\n' +
    '}\n';
  writeFileSync(VALIDATOR_PATH, typedSource);
  console.log(
    `wrote src/features/chat-widget/generated/chartContract.validator.ts (${typedSource.length} bytes)`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
